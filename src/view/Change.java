    private void BtnChgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnChgActionPerformed
        // TODO add your handling code here:
        int  change = ChangeTb.getSelectedRow();
        if (change == -1) 
        { JOptionPane.showMessageDialog(null,"Chọn 1 dòng để nhấn");}
         else {
            if(txtcheck()){
            ChildModel t = new ChildModel();
            t.setID(Integer.parseInt(txtID.getText()));
            t.setTenTre(txtName.getText());
            try{
                t.setNgaySinh(new SimpleDateFormat("dd/MM/yyyy").parse(txtDoB.getText()));
            }
            catch(ParseException ex){
            }
            t.setTenChuHo(txtPrs.getText());
            t.setGioiTinh((String) CbGen.getSelectedItem());
            t.setDiaChi(txtAddrs.getText());
            t.setTruong(txtSchool.getText());
            t.setLop(txtClass.getText());
            t.setThanhTich((String) CbRate.getSelectedItem());
            if(t.getThanhTich().equals("giỏi")){
                t.setSoLuongQua(8);
            }
            else if(t.getThanhTich().equals("khá")){
                t.setSoLuongQua(5);
            }try {
                t.setNgayNhanQua(new SimpleDateFormat("dd/MM/yyyy").parse(txtDoB.getText()));
            } catch (ParseException ex) {
                java.util.logging.Logger.getLogger(Add.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            }
            t.setLoaiQua("vở");
            controller = new ChangeController();
            try {
                if (controller.changeChild(list.get(change).getID(),t)) {
                    JOptionPane.showMessageDialog(null, "Thay đổi  thành công!!");                    
                }
            } catch (Exception e) {
                System.out.println(e.getMessage());
                JOptionPane.showMessageDialog(null, "Có lỗi xảy ra. Vui long kiểm tra lại!!", "Warning", JOptionPane.WARNING_MESSAGE);
            }
                try {
                    list = new ChangeController().getList();
                } catch (SQLException ex) {
                    java.util.logging.Logger.getLogger(Change.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    java.util.logging.Logger.getLogger(Change.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
                }
            controller.CreatAll(list, model);
            
        }
        }       
    }//GEN-LAST:event_BtnChgActionPerformed

    private void ChangeTbMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ChangeTbMouseClicked
        // TODO add your handling code here:
        int i = ChangeTb.getSelectedRow();
        if (list.isEmpty()){
           JOptionPane.showMessageDialog(null,"Danh sách trống"); 
        }else{
        txtName.setText(list.get(i).getTenTre());
        txtDoB.setText((String) new SimpleDateFormat("dd/MM/yyyy").format(list.get(i).getNgaySinh()));
        txtAddrs.setText(list.get(i).getDiaChi());
        txtPrs.setText(list.get(i).getTenChuHo());
        txtSchool.setText(list.get(i).getTruong());
        txtClass.setText(list.get(i).getLop());
        txtID.setText(Integer.toString(list.get(i).getID()));
        CbGen.setSelectedItem(list.get(i).getGioiTinh());
        CbRate.setSelectedItem(list.get(i).getThanhTich());}
    }//GEN-LAST:event_ChangeTbMouseClicked

